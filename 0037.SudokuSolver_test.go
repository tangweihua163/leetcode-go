func printMatrix(matrix [][]byte) {
	for i := 0; i < len(matrix); i++ {
		for j := 0; j < len(matrix); j++ {
			fmt.Printf("%c\t", matrix[i][j])
		}
		fmt.Println("")
	}
	fmt.Println("+++++++++++++++++++++++++++++")
}
